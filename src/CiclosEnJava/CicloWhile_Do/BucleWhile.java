package CiclosEnJava.CicloWhile_Do;

public class BucleWhile {

    /* while es booleano se pone una condicion y cuando se realiza se termina */
    public static void main(String[] args) {
        /* Ejemplo */
        var i = 1;
        while (i <=3) {
            System.out.println(i);
            i++;
        }

    }
}
